void main(List<String> args) {
  
  /// four types of operators 
  /// 1. unary prefix  2. unary postfix 3. binary infix 4. compound assignments
  
  int age = 30;
  print(--age);
  print(++age);
  print(-age);
  print(-(-age));
  print(!true);
  print(age);
  
  // unary bitwise complement prefix operator
  print(~1);
}