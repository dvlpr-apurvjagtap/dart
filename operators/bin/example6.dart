void main(List<String> args) {
  //compound assignment
  int age= 30;
  // print(age = 30);
  print(age ~/= 2); 
  /// any operator have = beside  right of it that means assigning 
  /// new calculated value to the variablevalue 
  /// this is compounding
  print(age*=2);
  print(age+=4);
  print(age&=2);
  print(age|=4);
  print(age^=10);
  print(age-=10);
  
}